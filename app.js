// Function to populate model dropdown based on selected company
function selectCompany() {
    model.innerHTML = `<option value="" selected disabled>select model</option>`;
    for (let key1 in allData[compay.value]) {
        model.innerHTML += `<option value='${key1}'>${allData[compay.value][key1].name}</option>`;
    }
}

// Function to filter out car based on selected company and model
